#include <stdio.h>

#define ZERO 0;

void	ft_bzero(void *s, size_t n)
{
	int			i;
	char		*str;

	i = 0;
	str = (char *)s;
	while (n > 0)
	{
		str[i] = ZERO;
		n--;
		i++;
	}
}
