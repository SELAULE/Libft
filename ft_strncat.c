char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;
	char *cat;

	i = 0;
	while (s1[i] != '\0')
	{
		cat[i] = s1[i];
		i++;
	}
	j = 0;
	while (n > 0)
	{
		cat[i] = s2[j];
		i++;
	}
	return (cat);
}
